-- Q1. 급여가 2000 초과인 사원들의 부서 정보, 사원 정보 출력
-- SQL-99 이전 방식 사용
SELECT E.DEPTNO, D.DNAME, E.EMPNO, E.ENAME, E.SAL
FROM EMP E, DEPT D
WHERE E.DEPTNO = D.DEPTNO(+)
AND SAL > 2000
ORDER BY DEPTNO, SAL ASC;
-- SQL-99 방식 사용
SELECT E.DEPTNO, D.DNAME, E.EMPNO, E.ENAME, E.SAL
FROM EMP E LEFT OUTER JOIN DEPT D ON (E.DEPTNO = D.DEPTNO)
WHERE SAL > 2000
ORDER BY DEPTNO, SAL ASC;

